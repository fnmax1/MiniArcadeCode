void draw_char(uint8_t curr_x_pos, uint8_t curr_y_pos, const char* ch, uint16_t bg_color, uint16_t font_color, fontsize font_size)
{
    uint8_t char_idx = *ch - 0x20;
    uint8_t char_width = (fontset[font_size][char_idx])->width;
    uint8_t char_height = (fontset[font_size][char_idx])->height;
    uint8_t bit_mask;
    uint8_t array_idx = 0;
    st7735s_set_window( curr_x_pos, ( curr_x_pos + (char_width - 1) ), curr_y_pos, ( curr_y_pos + (char_height - 1) ) );
    st7735s_send_command(RAMWR);
    DC_Set();
    CS_Clear();
    for(uint8_t row_cnt = 0; row_cnt < char_height; row_cnt++)
    {
        bit_mask = 0b10000000;
        for(uint8_t bit_cnt = 0; bit_cnt < char_width; bit_cnt++ )
        {
            if( ( (fontset[font_size][char_idx])->data[array_idx] ) & bit_mask )
            {
                Send_lower_Byte(bg_color);
                Buisy_Wait();
                Send_upper_Byte(bg_color);
                Buisy_Wait();
            }
            else
            {
                Send_lower_Byte(font_color);
                Buisy_Wait();
                Send_upper_Byte(font_color);
                Buisy_Wait();                
            }
            bit_mask >>= 1;
            if(char_width > 8 && bit_mask == 0)
            {
                bit_mask = 0b10000000;
                array_idx++;
            }
        }
        array_idx++;
    }
    CS_Set();
    txt_x_pos += char_width ;
}

void draw_char_transp(uint8_t curr_x_pos, uint8_t curr_y_pos, const char* ch, uint16_t font_color, fontsize font_size)
{
    uint8_t char_idx = *ch - 0x20;
    uint8_t char_width = (fontset[font_size][char_idx])->width;
    uint8_t char_height = (fontset[font_size][char_idx])->height;
    uint8_t bit_mask;
    uint8_t array_idx = 0;
    uint8_t pxl_x_pos;
    uint8_t pxl_y_pos = curr_y_pos;
    for(uint8_t row_cnt = 0; row_cnt < char_height; row_cnt++)
    {
        pxl_x_pos = curr_x_pos;
        bit_mask = 0b10000000;
        for(uint8_t bit_cnt = 0; bit_cnt < char_width; bit_cnt++ )
        {
            if( ( (fontset[font_size][char_idx])->data[array_idx] ) & bit_mask )
            {
                pxl_x_pos++;
            }
            else
            {
                draw_pixel(pxl_x_pos, pxl_y_pos, font_color);
                pxl_x_pos++;
            }
            bit_mask >>= 1;
            if(char_width > 8 && bit_mask == 0)
            {
                bit_mask = 0b10000000;
                array_idx++;
            }            
        }
        pxl_y_pos++;
        array_idx++;
    }
    txt_x_pos += char_width ;
}

void draw_string(uint8_t XS, uint8_t YS, const char* text, uint16_t bg_color, uint16_t font_color, uint8_t transp_bit, fontsize font_size)
{
    txt_x_pos = XS;
    txt_y_pos = YS;
    while(*text)
    {       
        if(transp_bit == 1)
        {
            draw_char_transp(txt_x_pos, txt_y_pos, text, font_color, font_size);
        }
        else if(transp_bit == 0)
        {
            draw_char(txt_x_pos, txt_y_pos, text, bg_color, font_color, font_size);
        }
        text++; 
    }
}

